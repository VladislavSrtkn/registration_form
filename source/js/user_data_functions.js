export class User {
  constructor(
    firstName,
    lastName,
    dateOfBirth,
    email,
    phoneNumber,
    gender,
    password
  ) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.dateOfBirth = dateOfBirth;
    this.email = email;
    this.phoneNumber = phoneNumber;
    this.gender = gender;
    this.password = password;
  }
}
